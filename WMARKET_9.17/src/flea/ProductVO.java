package flea;

public class ProductVO {

	private int pNumber;
	private String id;
	private String date_a;
	private String pName;
	private String image;
	private String pExplain;
	private String pCondition;
	private String price;
	private String date_b;
	private String available;
	private String bidPrice;
	
	public int getpNumber() {
		return pNumber;
	}
	public void setpNumber(int pNumber) {
		this.pNumber = pNumber;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getpExplain() {
		return pExplain;
	}
	public void setpExplain(String pExplain) {
		this.pExplain = pExplain;
	}
	public String getpCondition() {
		return pCondition;
	}
	public void setpCondition(String pCondition) {
		this.pCondition = pCondition;
	}
	public String getPrice() {
		return price;
	}
	public void setPrice(String price) {
		this.price = price;
	}
	public String getBidPrice() {
		return bidPrice;
	}
	public void setBidPrice(String bidPrice) {
		this.bidPrice = bidPrice;		
	}
	public String getDate_a() {
		return date_a;
	}
	public void setDate_a(String date_a) {
		this.date_a = date_a;
	}
	public String getDate_b() {
		return date_b;
	}
	public void setDate_b(String date_b) {
		this.date_b = date_b;
	}
	public String getAvailable() {
		return available;
	}
	public void setAvailable(String available) {
		this.available = available;
	}


	
}
